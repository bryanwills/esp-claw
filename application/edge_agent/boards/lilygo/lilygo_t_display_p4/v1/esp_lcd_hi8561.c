static esp_err_t hi8561_check_id(esp_lcd_panel_io_handle_t io)
{
    uint8_t id[2] = {0};
    uint16_t panel_id = 0;
    ESP_RETURN_ON_ERROR(esp_lcd_panel_io_rx_param(io, 0xda, &id[0], 1),
                        TAG, "read id high failed");
    ESP_RETURN_ON_ERROR(esp_lcd_panel_io_rx_param(io, 0xdb, &id[1], 1),
                        TAG, "read id low failed");
    panel_id = ((uint16_t)id[0] << 8) | id[1];
    ESP_RETURN_ON_FALSE(panel_id == HI8561_PANEL_ID || panel_id == HI8561_DCS_PANEL_ID,
                        ESP_FAIL, TAG, "unexpected id: 0x%04x", panel_id);
    ESP_LOGI(TAG, "HI8561 panel id: 0x%04x", panel_id);
    return ESP_OK;
}

static esp_err_t hi8561_send_init_cmds(esp_lcd_panel_io_handle_t io)
{
    for (size_t i = 0; i < sizeof(s_hi8561_init_cmds) / sizeof(s_hi8561_init_cmds[0]); i++) {
        const hi8561_lcd_init_cmd_t *cmd = &s_hi8561_init_cmds[i];
        ESP_RETURN_ON_ERROR(esp_lcd_panel_io_tx_param(io, cmd->cmd, cmd->data, cmd->data_len),
                            TAG, "send init command 0x%02x failed", cmd->cmd);
        if (cmd->delay_ms > 0) {
            vTaskDelay(pdMS_TO_TICKS(cmd->delay_ms));
        }
    }
    return ESP_OK;
}

esp_err_t esp_lcd_new_panel_hi8561(const esp_lcd_panel_io_handle_t io,
                                   const esp_lcd_panel_dev_config_t *panel_dev_config,
                                   esp_lcd_panel_handle_t *ret_panel)
{
    ESP_RETURN_ON_FALSE(io != NULL && panel_dev_config != NULL && ret_panel != NULL,
                        ESP_ERR_INVALID_ARG, TAG, "invalid arguments");
    const hi8561_vendor_config_t *vendor = panel_dev_config->vendor_config;
    ESP_RETURN_ON_FALSE(vendor != NULL && vendor->dsi_bus != NULL && vendor->dpi_config != NULL,
                        ESP_ERR_INVALID_ARG, TAG, "invalid vendor config");

    ESP_RETURN_ON_ERROR(esp_lcd_new_panel_dpi(vendor->dsi_bus, vendor->dpi_config, ret_panel),
                        TAG, "create DPI panel failed");
    ESP_RETURN_ON_ERROR(hi8561_check_id(io), TAG, "check id failed");
    ESP_RETURN_ON_ERROR(hi8561_send_init_cmds(io), TAG, "send init commands failed");
    return ESP_OK;
}
